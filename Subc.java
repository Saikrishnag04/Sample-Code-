class Subc
{
public static void main(String args[])
{
char color='o';
switch(color)
{
case'r':System.out.println("red");
break;
case'g':System.out.println("green");
break;
case'o':System.out.println("orange");
break;
case'v':System.out.println("violet");
break;
default:System.out.println("no color");
break;
}
}
}